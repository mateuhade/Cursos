# Operadores aritméticos em Python:
# + - soma
# - - subtração
# / - divisão decimal
# \ - divisão inteira
# * - multiplicação
# **- exponenciação
# % - Módulo, retorna o resto de uma divisão
#
# Operadores comparativos
# < - menor que
# > - maior que
# <=- menor ou igual que
# >=- maior ou igual que
# ==- igual
# !=- diferente (também pode-se usar <>)
#
# Operadores lógicos
# and- e
# or - ou
# not- não